				<h1>The Show</h1>
				<p>Maryland Masti 6 is an annual intercollegiate Garba/Raas competition held by the students at the University of Maryland, College Park. Our goals include spreading awareness of the traditional folk dance cultures of the West Indian State of Gujarat, providing an outlet for current students to express their passion for such dance, entertaining the guests at our show, and having a great weekend -- staff, teams, and audience alike!</p>
				
				<h2>About Garba/Raas</h2>
				<h3>What is Garba?</h3>
				<p>Garba is a popular folk dance native to the state of Gujarat, located in Western India. Traditionally, it is performed during a 9 day festival called Navratri (Nav = 9, Ratri = nights). Navratri is predominately a celebration in honor of Goddess Amba, as well as other popular Hindu gods and goddesses. It occurs in mid to late October, depending on the lunar calendar, and is divided into three sets of three days to worship different reincarnations of the Goddess Amba. The first three days are for Durga, the destroyer of all vices and impurities. The next three days are for Lakshmi, the bestower of wealth. The last three days are for Saraswati, the imparter of knowledge and wisdom. During Garba, a portrait of the goddess with a lit flame in front of her is placed in the center of the circle. People then dance around the deity clapping rhythmically to the accompanying music. This traditional form has transformed into the modern form of Garba that we see today.</p>
				
				<h3>What is Raas?</h3>
				<p>Raas, a highly energetic and fast-paced dance, is associated with Garba and is also performed during Navratri. Traditionally, Raas was only performed by men, but it has evolved into a dance for everyone. Raas is played with two sticks, each about 1.5 feet long. The sticks are hit together to the beat of the accompanying song and people move from partner to partner in a choreographed dance.</p>
				
